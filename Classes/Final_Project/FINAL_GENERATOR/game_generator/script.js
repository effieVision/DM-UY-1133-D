function writeToPage(incomingText) {
  globalDelay += 60;
  setTimeout(function() {
    outputElement.innerHTML += "<pre>" + incomingText + "</pre>";
    window.scrollTo(0, document.body.scrollHeight);

  }, globalDelay);

}
